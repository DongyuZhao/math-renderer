import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { createRequire } from "node:module";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const outputPath = join(repoRoot, "packages/mathjax-core/generated/mathjax-core-runtime.js");
const requireFromReact = createRequire(join(repoRoot, "packages/react-math/package.json"));
const mathJaxPackagePath = requireFromReact.resolve("mathjax-full/package.json");
const mathJaxRoot = dirname(mathJaxPackagePath);
const mathJaxPackage = JSON.parse(readFileSync(mathJaxPackagePath, "utf8"));
const sourceFiles = [
    "es5/startup.js",
    "es5/core.js",
    "es5/input/tex.js",
    // Chemistry (\ce{...}) support; must load after the base tex input.
    "es5/input/tex/extensions/mhchem.js",
    "es5/output/svg.js",
    "es5/output/svg/fonts/tex.js",
    "es5/adaptors/liteDOM.js"
];

function buildRuntimeBundle() {
    const sourcePaths = sourceFiles.map((sourceFile) => join(mathJaxRoot, sourceFile));
    const header = [
        `// Generated from mathjax-full@${mathJaxPackage.version} by scripts/build-mathjax-core-runtime.mjs.`,
        `// Source components: ${sourceFiles.join(", ")}.`,
        "// Do not edit this file by hand.",
        ""
    ];

    const body = sourcePaths.map((sourcePath) => {
        const source = readFileSync(sourcePath, "utf8").trimEnd();
        return [
            `// Begin ${relative(mathJaxRoot, sourcePath)}`,
            source,
            `// End ${relative(mathJaxRoot, sourcePath)}`
        ].join("\n");
    });

    return [...header, nativeStartupPrelude(), ...body, ""].join("\n");
}

function nativeStartupPrelude() {
    return [
        "// Disable MathJax component auto-startup; shared latex-to-svg.js selects the adaptor.",
        "(function (global) {",
        "  var config = global.MathJax || {};",
        "  config.startup = config.startup || {};",
        "  config.startup.typeset = false;",
        "  config.startup.ready = function () {};",
        "  global.MathJax = config;",
        "}(this));",
        ""
    ].join("\n");
}

function writeRuntime() {
    const output = buildRuntimeBundle();
    mkdirSync(dirname(outputPath), { recursive: true });
    writeFileSync(outputPath, output);
    console.log(
        `generated packages/mathjax-core/generated/mathjax-core-runtime.js from mathjax-full@${mathJaxPackage.version}`
    );
}

function checkRuntime() {
    const expected = buildRuntimeBundle();
    const actual = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : "";
    if (actual !== expected) {
        console.error(
            "packages/mathjax-core/generated/mathjax-core-runtime.js is out of date; run npm run build:mathjax-core-runtime"
        );
        process.exitCode = 1;
        return;
    }
    console.log("mathjax-core-runtime.js is up to date");
}

if (process.argv.includes("--check")) {
    checkRuntime();
} else {
    writeRuntime();
}
