// Prepares the Android pixel-snapshot inputs for compose-math's Roborazzi tests:
//   1. Copies the shared golden SVGs into the unit-test resources (classpath).
//   2. Generates GoldenCorpus.kt — the (id, fontSize) list the test iterates.
//
// Run after changing the corpus or regenerating goldens:
//   npm run gen:compose-goldens

import { copyFileSync, mkdirSync, readdirSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import { loadFormulas } from "../packages/mathjax-core/fixtures/formulas.mjs";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const goldenSrc = join(repoRoot, "packages/mathjax-core/test/golden/svg");
const testRoot = join(repoRoot, "packages/compose-math/src/test");
const resourcesDir = join(testRoot, "resources/golden");
const corpusFile = join(testRoot, "kotlin/io/github/dongyuzhao/composemath/GoldenCorpus.kt");

const formulas = loadFormulas();

// Sync golden SVGs onto the unit-test classpath.
rmSync(resourcesDir, { recursive: true, force: true });
mkdirSync(resourcesDir, { recursive: true });
for (const name of readdirSync(goldenSrc)) {
    if (name.endsWith(".svg")) {
        copyFileSync(join(goldenSrc, name), join(resourcesDir, name));
    }
}

const entries = formulas
    .map((formula) => `    GoldenFormulaEntry("${formula.id}", ${formula.fontSize}f),`)
    .join("\n");

const output = `package io.github.dongyuzhao.composemath

// GENERATED by scripts/gen-compose-goldens.mjs from
// packages/mathjax-core/fixtures/common-formulas.tsv. Do not edit by hand.
// One entry per corpus formula; the Roborazzi test rasterizes each golden SVG
// (loaded from test resources) and captures <id>.png.

internal data class GoldenFormulaEntry(val id: String, val fontSizeSp: Float)

internal val goldenFormulas: List<GoldenFormulaEntry> = listOf(
${entries}
)
`;

mkdirSync(dirname(corpusFile), { recursive: true });
writeFileSync(corpusFile, output);
console.log(`synced ${formulas.length} golden SVGs -> src/test/resources/golden`);
console.log(`generated GoldenCorpus.kt with ${formulas.length} entries`);
