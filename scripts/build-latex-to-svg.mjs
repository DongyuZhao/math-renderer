import { createRequire } from "node:module";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const outputPath = join(repoRoot, "packages/mathjax-core/generated/latex-to-svg.js");
const coreSourcePath = join(repoRoot, "packages/mathjax-core/src/latex-to-svg-core.ts");
const nativeSourcePath = join(repoRoot, "packages/mathjax-core/src/native-latex-to-svg.ts");
const requireFromReact = createRequire(join(repoRoot, "packages/react-math/package.json"));
const ts = requireFromReact("typescript");

function compile(sourcePath) {
    const source = readFileSync(sourcePath, "utf8");
    return ts
        .transpileModule(source, {
            compilerOptions: {
                esModuleInterop: true,
                module: ts.ModuleKind.CommonJS,
                removeComments: false,
                strict: true,
                target: ts.ScriptTarget.ES2019
            },
            fileName: sourcePath
        })
        .outputText.trimEnd();
}

function buildLatexToSvgIIFE() {
    const compiledCore = compile(coreSourcePath);
    const compiledNativeHost = compile(nativeSourcePath);

    return [
        "// Generated from packages/mathjax-core/src/latex-to-svg-core.ts and packages/mathjax-core/src/native-latex-to-svg.ts by scripts/build-latex-to-svg.mjs.",
        "// Do not edit this file by hand.",
        "(function (global) {",
        "  var coreExports = {};",
        "  var coreModule = { exports: coreExports };",
        "  var exports = coreExports;",
        "  var module = coreModule;",
        indent(compiledCore),
        "  coreExports = coreModule.exports;",
        "  var LatexToSvgCore = coreExports;",
        "  var hostExports = {};",
        "  var hostModule = { exports: hostExports };",
        "  exports = hostExports;",
        "  module = hostModule;",
        indent(compiledNativeHost),
        "  hostExports = hostModule.exports;",
        '  if (typeof hostExports.installLatexToSvgBridge !== "function") {',
        '    throw new Error("Generated LaTeX-to-SVG bridge is missing installLatexToSvgBridge().");',
        "  }",
        "  hostExports.installLatexToSvgBridge(global);",
        "}(this));",
        ""
    ].join("\n");
}

function indent(value) {
    return value
        .split("\n")
        .map((line) => (line.length > 0 ? `  ${line}` : line))
        .join("\n");
}

function writeLatexToSvg() {
    const output = buildLatexToSvgIIFE();
    mkdirSync(dirname(outputPath), { recursive: true });
    writeFileSync(outputPath, output);
    console.log(
        "generated packages/mathjax-core/generated/latex-to-svg.js from mathjax-core TypeScript"
    );
}

function checkLatexToSvg() {
    const expected = buildLatexToSvgIIFE();
    const actual = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : "";
    if (actual !== expected) {
        console.error(
            "packages/mathjax-core/generated/latex-to-svg.js is out of date; run npm run build:latex-to-svg"
        );
        process.exitCode = 1;
        return;
    }
    console.log("latex-to-svg.js is up to date");
}

if (process.argv.includes("--check")) {
    checkLatexToSvg();
} else {
    writeLatexToSvg();
}
